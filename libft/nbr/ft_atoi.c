#include "../libft.h"

int	ft_atoi(const char *s)
{
	int	sign;
	int	res;

	res = 0;
	sign = 1;
	if (!s)
		return (0);
	while (*s == ' ' || (*s >= 9 && *s <= 13))
	{
		s++;
	}
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = sign * -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		res = (res * 10) + (*s - '0');
		s++;
	}
	return (res * sign);
}
