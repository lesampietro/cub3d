#include "../libft.h"

void free_gnl_cache(char **cache)
{
	int i;

	i = 0;
	while (i < 1024)
	{
		if (cache[i])
		{
			free(cache[i]);
			cache[i] = NULL;
		}
		i++;
	}
}
