#include "../libft.h"

void	free_gnl_cache(int fd, char **cache)
{
	int	i;

	i = 0;
	if (fd == -2)
	{
		while (i < 1024)
		{
			if (cache[i])
			{
				free(cache[i]);
				cache[i] = NULL;
			}
			i++;
		}
	}
}
